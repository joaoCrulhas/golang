//Response provided by pokeapi
type Response struct {
	Count   int       `json:"count"`
	Next    string    `json:"next"`
	Results []Pokemon `json:"results"`
}

//Pokemon with name and url
type Pokemon struct {
	Name string `json:"name"`
	URL  string `json:"url"`
}

func getPokemon(id int, pokemons []Pokemon) PokemonDetail {
	fmt.Printf("Getting data from %v \n", pokemons[id].Name)
	resp, err := http.Get(pokemons[id].URL)
	if err != nil {
		log.Fatalln(err)
	}
	body, err := ioutil.ReadAll(resp.Body)

	if err != nil {
		log.Fatalln(err)
	}
	var pokemonDetail PokemonDetail
	json.Unmarshal(body, &pokemonDetail)
	return pokemonDetail
}

func main() {
	jsonFile, err := os.Open("pokemons.json")
	if err != nil {
		fmt.Println(err)
	}
	fmt.Println("Successfully Opened pokemons.json")
	byteValue, _ := ioutil.ReadAll(jsonFile)

	var response Response

	json.Unmarshal(byteValue, &response)

	for i := 0; i < len(response.Results); i++ {
		pokemon := (strings.Title(response.Results[i].Name))
		fmt.Printf("Indice => %v, Pokemon => %v || URL => %v \n", i, pokemon, response.Results[i].URL)
	}
	fmt.Println("Enter the pokemon that you want know, based on index please!: ")
	var index int
	if index > len(response.Results) {
		log.Fatal("Not in our database!")
		return
	}
	fmt.Scanln(&index)
	pokemonDetail := getPokemon(index, response.Results)
	fmt.Println(pokemonDetail)
}
